def isRelevant(cycleNumber) {
    (cycleNumber - 20) % 40 == 0 && cycleNumber <= 220
}

def sumOfSignalStrengths(List<String> lines) {
    def totalStrength = 0
    def x = 1
    def cycleNumber = 1
    lines.each {
        if(isRelevant(cycleNumber)) {
            totalStrength += x*cycleNumber
        }
        if(it == 'noop') {
            cycleNumber++
        }
        else {
            def amount = (it - 'addx ') as int
            if(isRelevant(cycleNumber+1)) {
                totalStrength += x*(cycleNumber+1)
            }
            x += amount
            cycleNumber += 2
        }   
    }
    totalStrength
}

